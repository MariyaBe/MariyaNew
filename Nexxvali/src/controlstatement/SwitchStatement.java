package controlstatement;

public class SwitchStatement {


	public static void main(String[] args) {

		
	int var = 9;
	
	switch(var) {
	case 10:
	System.out.println("value is 10");
	break;
	case 9:
	System.out.println("value is 9");
	break;
	case 8:
	System.out.println("value is 8");
    break;
    
	}	
	
	String day = "Monday";

	switch(day) {
	case "Sunday":
	System.out.println("value is Sunday");
	break;
	case "Monday":
		System.out.println("value is Monday");
		break;
	case "Tuesday" :
		System.out.println("value is Sunday");
		break;

	}
	}
	
	}

